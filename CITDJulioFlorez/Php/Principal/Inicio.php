        <h5 class="Title"> Colegio I.T.D. Julio Florez </h5>
        <p class="Text">
            El Colegio Instituto Técnico Distrital Julio Flórez es una institución educativa oficial de carácter
            técnico, pionera en la implementación de la jornada única en el Distrito Capital desde el año 2015, que ha
            planteado como política institucional la formación integral con calidad de sus educandos, preparándolos a
            las competencias del siglo XXI. Ofreciendo diversidad de planes y programas en cada uno de sus ciclos
            formativos, entre los que se destacan, sus cinco especialidades (Administración de Empresas, Programación de
            Software, Manejo Ambiental, Diseño e Integración Multimedial, Administracion Hotelera y Turistica)
            articuladas con diferentes IES (Instituciones de Educación Superior), la formación deportiva en centros de
            interés acompañada por docentes del IDRD y el desarrollo de un plan de estudios enriquecido en los campos de
            formación matemático, de la comunicación, la ciencia, la tecnología y el arte (música y danza), el
            desarrollo de la política de primera infancia y el fortalecimiento del inglés como segunda lengua.

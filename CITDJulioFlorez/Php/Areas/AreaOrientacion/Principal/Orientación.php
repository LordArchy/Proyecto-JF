        <h5 class="Title"> Educación educativa </h5>
        <p class="Text">
            La orientación educativa pretende ayudar al correcto desarrollo del alumnado mediante la personalización del
            proceso de enseñanza-aprendizaje adaptado a las características de cada individuo y asesorando a estudiantes
            y
            familias.
            <br>
            <br>
            <img src="Images/Orientacion/OrientacionEducativa.jpg" alt="">
            Desde los centros escolares se fomenta la educación, pero también el desarrollo a nivel personal y social.
            Por
            eso, las escuelas no solo trabajan los contenidos de las diferentes materias, sino también ayudan al
            correcto
            desenvolvimiento de los niños, a mantener una comunicación y relación constante con las familias, a
            desarrollar
            habilidades y competencias en los estudiantes, etc. En este contexto, es importante el papel de los docentes
            y
            del equipo de trabajo del centro, donde destaca la función de orientación educativa, la cual tiene lugar
            desde
            las aulas y también mediante el apoyo del departamento de orientación de un centro educativo.
        </p>
</main>
